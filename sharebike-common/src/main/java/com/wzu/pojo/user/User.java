package com.wzu.pojo.user;

/**
 * @author jack
 * @date 2017/10/16   17:15
 */
public class User {

    private String id;

    private Integer age;

    private String name;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
